    <h1>Unico punto de acceso</h1>
    <p>El framework MVC (Modelo-Vista-Controlador) es una arquitectura de software que divide la lógica de una aplicación en tres componentes principales:</p>
    <ul>
        <li>Modelo (Model): Encarga de la lógica de negocio y la manipulación de datos.</li>
        <li>Vista (View): Se encarga de mostrar la información al usuario.</li>
        <li>Controlador (Controller): Actúa como intermediario entre el modelo y la vista, gestionando las solicitudes del usuario y las respuestas.</li>
    </ul>
